public class Contribution
{
    public static void main(String[] args)
    {
        //Declare a double variable monthlyPay and initialize the variable with a value of 6000.0
        double monthlyPay = 6000.0;
        //Declare a double variable contribution
        double contribution;
        //Calculate and display a 5% contribution
        contribution = monthlyPay * 0.05;
        //Calculate and Display an 8% Contribution
        contribution = monthlyPay * 0.08;
        //Calculate and display a 10% contribution
        contribution = monthlyPay * 0.10;
        
        //display the contribution
        System.out.println("10% contribution is $" + contribution + " per month");
        
    }        
        
}
